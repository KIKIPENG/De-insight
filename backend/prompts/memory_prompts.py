# 對話結束後，從使用者發言抽取記憶條目
MEMORY_EXTRACTION_PROMPT = """
你是一個記憶整理者。

你會收到一段對話，從「創作者說的話」裡，找出值得長期記憶的內容。
只抽創作者說的話，不抽 AI 的回應。

只抽以下三類，有則抽，無則回傳空陣列：
1. insight（洞見）：對藝術、設計、創作的獨到觀察
2. question（問題）：深度問題，不是閒聊
3. reaction（反應）：對作品、藝術家、風格的感性反應

每條記錄附帶 topic（主題分類），從以下選擇：
藝術史、設計史、批判理論、美學、媒材技法、個人創作、當代藝術、
建築、攝影、身體感知、資本主義、後殖民、女性主義、機構批判、策展

不值得記憶的：閒聊、問候、操作性問題、太模糊的發言。

使用者的話：
{user_text}

只回傳 JSON 陣列：
[{{"type": "insight"|"question"|"reaction", "content": "精簡摘要", "topic": "主題"}}]

沒有值得記錄的內容就回傳：[]
""".strip()

